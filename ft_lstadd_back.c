#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)

{
	t_list	*temp;

	if (!*lst)
		*lst = new;
	else
	{
		temp = *lst;
		while (temp->next)
			temp = temp->next;
		temp->next = new;
	}
}
